public class string {
    public static void main(String[] args) {
        String a="Akash is a boy";
        String b="He loves Java.";
        System.out.println("Size of the string a is: "+ a.length());
        System.out.println("Size of the string b is: "+ b.length());
        System.out.println("New string is: "+ a +" "+b);
        System.out.println("Size of new string is: "+ (a+b).length());
    }
}
